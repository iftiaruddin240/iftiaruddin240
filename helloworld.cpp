#include<bits/stdc++.h>
using namespace std;
int main()
{
    int integerType;
    char charType;
    float floatType;
    double doubleType;

    cout<<"Size of int is:"<<sizeof(integerType)<<endl;

    cout<<"Size of char is:"<<sizeof(charType);
    cout<<endl;
    cout<<"Size of float is:"<<sizeof(float);
    cout<<endl;
    cout<<"Size of double is:"<<sizeof(double);
    cout<<endl;

    return 0;
}
